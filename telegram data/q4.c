/*Write a program to print a star pattern using multiple printf and single printf
    *
    **
    ***
    ****
    *****
*/
#include<stdio.h>
int main(){
    printf("The star pattern is as follows:\n");
    printf("*\n");
    printf("**\n");
    printf("***\n");
    printf("****\n");
    printf("*****\n");
    // for single printf
    printf("*\n**\n***\n****\n*****\n"); 
    return 0;
}
